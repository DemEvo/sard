let currentLang = 'ru';

function getTrans(key, params = {}) {
    let text = translations[currentLang][key] || key;
    for (let k in params) {
        text = text.replace(`{${k}}`, params[k]);
    }
    return text;
}

function updateTranslations() {
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (translations[currentLang] && translations[currentLang][key]) {
            el.innerText = translations[currentLang][key];
        }
    });
}

let currentProjectId = null;
let projectState = null;

async function bootstrap() {
    updateTranslations();
    const res = await fetch('/api/projects');
    const projects = await res.json();

    if (projects.length > 0) {
        await loadProject(projects[0].id);
    } else {
        await createNewProject();
    }
}

async function createNewProject() {
    const res = await fetch('/api/projects', {
        method: 'POST',
        headers: { 'Content-Type': 'application/json' },
        body: JSON.stringify({ title: getTrans('new_project') })
    });
    const proj = await res.json();
    await loadProject(proj.id);
}

async function loadProject(id) {
    currentProjectId = id;
    const res = await fetch(`/api/projects/${id}`);
    projectState = await res.json();
    renderProjectSelector();
    renderAccordion();
    renderFiles();
}

function renderProjectSelector() {
    // Optionally implemented for selecting multiple projects
    // For MVP just show project name
    const sel = document.getElementById('project-selector');
    sel.innerHTML = `<strong>${getTrans('label_project')}:</strong> ${projectState.project.title} (ID: ${projectState.project.id})`;
}

function renderAccordion() {
    const container = document.getElementById('accordion-container');
    container.innerHTML = '';

    const currStateIdx = STATES.indexOf(projectState.project.current_state);

    STATES.forEach((state, idx) => {
        const block = projectState.blocks.find(b => b.state_id === state);
        const isCompleted = idx < currStateIdx;
        const isActive = idx === currStateIdx;
        const isLocked = idx > currStateIdx;

        const panel = document.createElement('div');
        panel.className = `accordion-panel ${isActive ? 'active' : ''} ${isLocked ? 'locked' : ''}`;

        const header = document.createElement('div');
        header.className = 'accordion-header';

        let statusBadge = '';
        if (isCompleted) statusBadge = getTrans('status_approved');
        else if (isActive) statusBadge = getTrans('status_active');
        else statusBadge = getTrans('status_locked');

        let readableState = state.replace(/_/g, ' ');

        header.innerHTML = `
            <span>${idx + 1}. ${readableState}</span>
            <span>${statusBadge}</span>
        `;

        if (!isLocked) {
            header.onclick = () => {
                document.querySelectorAll('.accordion-panel').forEach(p => p.classList.remove('active'));
                panel.classList.add('active');
            };
        }

        const content = document.createElement('div');
        content.className = 'accordion-content';

        if (isActive) {
            content.innerHTML = renderActiveState(state, block);
            // Render existing chats
            const stateChats = projectState.chats.filter(c => c.state_id === state);
            setTimeout(() => {
                const cb = document.getElementById(`chat-box-${state}`);
                if (cb) {
                    stateChats.forEach(c => appendMessageToChat(cb, c));
                    cb.scrollTop = cb.scrollHeight;
                }
            }, 50);
        } else if (isCompleted) {
            content.innerHTML = renderCompletedState(state, block);
        } else {
            content.innerHTML = `<p>${getTrans('msg_locked_stage')}</p>`;
        }

        panel.appendChild(header);
        panel.appendChild(content);
        container.appendChild(panel);
    });
}

function renderActiveState(stateId, blockDraft) {
    const defaultDraft = blockDraft ? blockDraft.content : '';
    const hintText = HINTS[currentLang][stateId] || '';
    const hintLabel = currentLang === 'ru' ? 'Подсказка' : 'Hint';
    return `
        <div class="state-hint" style="background: rgba(88, 166, 255, 0.1); padding: 1rem; border-left: 4px solid var(--primary); border-radius: 4px; margin-bottom: 1rem;">
            <strong>💡 ${hintLabel}:</strong> ${hintText}
        </div>
        <div class="chat-box" id="chat-box-${stateId}">
            <div class="message model">
                ${getTrans('msg_greeting', { stateId: stateId })}
            </div>
        </div>
        <div class="chat-input" style="align-items: flex-start;">
            <textarea id="chat-msg-${stateId}" placeholder="${getTrans('placeholder_chat')}" onkeydown="handleEnter(event, '${stateId}')"></textarea>
            <button class="primary" onclick="sendMessage('${stateId}')">${getTrans('btn_send')}</button>
        </div>
        <hr style="border:0; border-top:1px solid var(--border); margin: 1.5rem 0;" />
        <div style="display:flex; justify-content: space-between; align-items: center; margin-bottom: 0.5rem;">
            <h3 style="margin:0;">${getTrans('title_draft')}</h3>
            <span style="font-size: 0.85em; color: var(--locked);">${getTrans('desc_auto')}</span>
        </div>
        <textarea id="draft-content-${stateId}" rows="8">${defaultDraft}</textarea>
        <button class="primary" style="margin-top:1rem; width: 100%;" onclick="saveBlock('${stateId}')">${getTrans('btn_approve')}</button>
    `;
}

function renderCompletedState(stateId, block) {
    let statusWarn = '';
    if (block && block.status === 'outdated') {
        statusWarn = `<p style="color:#ff7b72; margin-top:0;">${getTrans('warn_outdated')}</p>`;
    }

    return `
        ${statusWarn}
        <textarea rows="10" style="width:100%" readonly>${block ? block.content : ''}</textarea>
        <button style="margin-top:1rem;" onclick="editBlock('${stateId}', ${block ? block.id : null})">
            ${getTrans('btn_edit')}
        </button>
    `;
}

function appendMessageToChat(chatBox, messageObj) {
    const p = document.createElement('div');
    p.className = `message ${messageObj.role}`;

    // ВАЖНО: Это CSS-свойство заставляет браузер отображать реальные переносы строк
    p.style.whiteSpace = 'pre-wrap';

    if (messageObj.role === 'user') {
        p.innerText = messageObj.content;
    } else {
        try {
            const parsed = JSON.parse(messageObj.content);

            // Если ИИ вернул экранированную строку (как в твоем примере)
            if (typeof parsed === 'string') {
                p.innerText = parsed;
            }
            // Если ИИ вернул диалог агентов (стандартный сценарий)
            else if (parsed.agents_dialogue) {
                p.innerHTML = `
                    <div style="margin-bottom: 10px;"><strong>🤖 ${getTrans('label_cognitive')}:</strong></div>
                    ${parsed.agents_dialogue.map(a => `<div style="margin-bottom:5px;"><em>${a.agent}</em>: ${a.message}</div>`).join('')}
                    <hr style="border-top:1px solid rgba(255,255,255,0.1);">
                    <div style="color: var(--text-color);"><strong>👑 ${getTrans('label_facilitator')}:</strong> ${parsed.facilitator_summary}</div>
                `;

                const draftEl = document.getElementById(`draft-content-${messageObj.state_id}`);
                if (draftEl && parsed.facilitator_summary) {
                    draftEl.value += (draftEl.value ? '\n\n' : '') + parsed.facilitator_summary;
                }
            }
            // Для любого другого JSON
            else {
                p.innerText = JSON.stringify(parsed, null, 2);
            }
        } catch (e) {
            // Если пришел чистый текст, а не JSON
            p.innerText = messageObj.content;
        }
    }
    chatBox.appendChild(p);
}

async function sendMessage(stateId) {
    const msgInput = document.getElementById(`chat-msg-${stateId}`);
    const msg = msgInput.value.trim();
    if (!msg) return;

    msgInput.value = '';
    msgInput.disabled = true;

    const chatBox = document.getElementById(`chat-box-${stateId}`);

    // Optimistic UI for User
    appendMessageToChat(chatBox, { role: 'user', content: msg });
    chatBox.scrollTop = chatBox.scrollHeight;

    // Optimistic UI for Model Thinking
    const mMsg = document.createElement('div');
    mMsg.className = 'message model';
    mMsg.id = 'thinking-indicator';
    mMsg.innerText = getTrans('msg_thinking');
    chatBox.appendChild(mMsg);
    chatBox.scrollTop = chatBox.scrollHeight;

    try {
        const res = await fetch(`/api/projects/${currentProjectId}/chat`, {
            method: 'POST',
            headers: { 'Content-Type': 'application/json' },
            body: JSON.stringify({ message: msg, state_id: stateId })
        });
        const reply = await res.json();

        chatBox.removeChild(document.getElementById('thinking-indicator'));
        appendMessageToChat(chatBox, reply);
        chatBox.scrollTop = chatBox.scrollHeight;

    } catch (err) {
        document.getElementById('thinking-indicator').innerText = getTrans('err_api');
    } finally {
        msgInput.disabled = false;
        msgInput.focus();
    }
}

async function saveBlock(stateId) {
    const draftContent = document.getElementById(`draft-content-${stateId}`).value;
    if (!draftContent) {
        alert(getTrans('err_empty_block'));
        return;
    }

    await fetch(`/api/projects/${currentProjectId}/block`, {
        method: 'POST',
        headers: { 'Content-Type': 'application/json' },
        body: JSON.stringify({ state_id: stateId, content: draftContent })
    });

    await loadProject(currentProjectId);
}

function editBlock(stateId, blockId) {
    if (confirm(getTrans('confirm_edit'))) {
        // Find block content
        const block = projectState.blocks.find(b => b.state_id === stateId);

        // Change project state locally and save
        fetch(`/api/projects/${currentProjectId}/block`, {
            method: 'POST',
            headers: { 'Content-Type': 'application/json' },
            body: JSON.stringify({ state_id: stateId, content: block.content })
        }).then(() => {
            loadProject(currentProjectId);
        });
    }
}

function handleEnter(e, stateId) {
    if (e.key === 'Enter' && !e.shiftKey) {
        e.preventDefault();
        sendMessage(stateId);
    }
}

async function uploadFile() {
    const fileInput = document.getElementById('file-input');
    const statusDiv = document.getElementById('upload-status');
    const file = fileInput.files[0];
    if (!file) {
        statusDiv.innerHTML = `<span style="color:#ff7b72;">${getTrans('err_no_file')}</span>`;
        return;
    }

    const formData = new FormData();
    formData.append('file', file);

    statusDiv.innerHTML = `<span style="color:var(--primary);">${getTrans('msg_distilling')}</span>`;
    const res = await fetch(`/api/projects/${currentProjectId}/upload`, {
        method: 'POST',
        body: formData
    });

    if (res.ok) {
        fileInput.value = '';
        statusDiv.innerHTML = `<span style="color:var(--success);">${getTrans('msg_upload_ok')}</span>`;
        setTimeout(() => { statusDiv.innerHTML = ''; }, 3000);
        await loadProject(currentProjectId);
    } else {
        statusDiv.innerHTML = `<span style="color:#ff7b72;">${getTrans('msg_upload_fail')}</span>`;
    }
}

function renderFiles() {
    const fileList = document.getElementById('file-list');
    fileList.innerHTML = '';
    projectState.files.forEach(f => {
        const li = document.createElement('li');
        li.innerText = f.filename;
        fileList.appendChild(li);
    });
}

